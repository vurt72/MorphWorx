// ============================================================================
// Pattern Data for Trigonomicon Drum Trigger Module
// ============================================================================
//
//     - Odd time signatures (all voices at 14, 24, 28 steps, etc.)
//     - Polymetric patterns (each voice at a different length)
//     - High-resolution patterns (64 steps for 32nd note detail)
//
// Voices: 0=Kick, 1=Snare1, 2=Snare2, 3=ClosedHiHat, 4=OpenHiHat, 5=Ride/Crash
//
// Probability values:
//   1.0  = always fires
//   0.0  = never fires
//   0.7  = likely hit
//   0.5  = coin flip (variation / ghost accent)
//   0.3  = light ghost / occasional
//   0.2  = rare texture
//
// 130 patterns organized as:
//   0-5:   Amen / breakbeat flagship bank
//   6-7:   Jungle flagship bank
//   8-9:   Breakcore flagship bank
//   10-12: Additional amen-focused patterns with Ride/Crash support
//   13-15: Additional jungle-focused patterns with Ride/Crash support
//   16-19: Additional breakcore-focused patterns with Ride/Crash support
//   20-39: Jungle, Breakcore, Amen expansions
//   40-44: Drum & Bass / Jungle
//   45-49: UK Garage
//   50-54: Dub / Dubwise / Reggae influenced
//   55-59: IDM / Drillcore
//   60-79: EBM / Industrial Dance
//   80-89: Electro / Detroit Style
//   90-99: Techno
//   100-109: Gqom / Durban Broken Beat
//   110-119: Gqom / Durban (Extended)
//   120-129: Trap
// ============================================================================

